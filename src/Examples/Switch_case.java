package Examples;

public class Switch_case {
	public static void main(String[] args) {
		switch(1) {
		case 1:
		{
			System.out.println("One");
			break;
		}
		case 2:
		{
			System.out.println("Two");
			break;
		}
		default:
		{
			System.out.println("default");
			break;
		}
		}
		
	}
}
